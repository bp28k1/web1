<h3>Starship</h3>

<p>Az űreszköz gyökeresen megváltoztatja az űrutazást, hiszen egy újfajta űrszállítmányozási rendszer épül ki általa, mely interkontinentális és bolygóközi utazásoknak ad teret, a költségeket jelentős mértékben csökkentve. Ezzel a nagyteljesítményű rakétával - a világ legerősebb rakétája lesz - kívánják kiváltani a Falcon rakétacsaládot és a Dragon űrhajót.</p>

<p>A rakétarendszer teljesen újrafelhasználható módban több mint 100 tonna hasznos terhet tud majd alacsony Föld körüli pályára juttatni. A rakéta fejlesztése a vállalat két texasi (McGregor és Boca Chica), valamint floridai (Space Coast) telephelyén történik.</p>

