<h3>Starship története</h3>

<p>A Starship (korábbi nevén Starship-Super Heavy vagy BFR) a SpaceX újgenerációs szupernehéz rakétarendszere, mely arra lett tervezve, hogy az emberiség a Naprendszeren belüli égitestekre könnyen eljuthasson. A felső fokozatot Starship-nek, az alsót Super Heavy-nek hívják. A rakétarendszert összefoglaló néven Starshipnek nevezzük.</p>

